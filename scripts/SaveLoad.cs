using UnityEngine;
using System.Collections;
using System.Collections.Generic;
using System.Runtime.Serialization.Formatters.Binary;
using System.IO;

public static class SaveLoad {

		public static List<MainData> savedGames = new List<MainData>();

		//методы загрузки и сохранения статические, поэтому их можно вызвать откуда угодно
		public static void Save() {
				SaveLoad.savedGames.Add(MainData.current);
				BinaryFormatter bf = new BinaryFormatter();
				//Application.persistentDataPath это строка; выведите ее в логах и вы увидите расположение файла сохранений
				FileStream file = File.Create (Application.persistentDataPath + "/savedGames.gd");
				bf.Serialize(file, SaveLoad.savedGames);
				file.Close();
		}   

		public static void Load() {
				if(File.Exists(Application.persistentDataPath + "/savedGames.gd")) {
						BinaryFormatter bf = new BinaryFormatter();
						FileStream file = File.Open(Application.persistentDataPath + "/savedGames.gd", FileMode.Open);
						SaveLoad.savedGames = (List<MainData>)bf.Deserialize(file);
						file.Close();
				}
		}
}
